public class Repetition {
    //while loop,Do-while,for loop
    public static void main(String[] args) {
        int counter = 0;
        while (counter < 10) {
            System.out.println(counter);
            counter++;
        }
        for ( counter = 0; counter < 10; counter++) {
            System.out.println(counter);
        }

    }
}
